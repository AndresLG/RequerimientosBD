/* LISTADO DE LAS VIVIENDAS CON SU RESPECTIVO ESTADO Y SI ESTÁN DISPONIBLES */
SELECT VIVIENDA.CODVIV AS VIVIENDA, VIVIENDA_ESTADO.DESESTVIV AS ESTADO
FROM VIVIENDA
INNER JOIN VIVIENDA_ESTADO
ON VIVIENDA_ESTADO.CODVIVEST = VIVIENDA.CODVIVEST
;
