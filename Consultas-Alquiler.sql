/* LISTADO DE LOS INQUILINOS QUE TIENEN MÁS CASAS ALQUILADAS */
SELECT ALQUILER_DETALLE.CODALQDET,Concat(PERSONA.NOMPER,concat(' ',concat(PERSONA.APEPATPER,concat(' ',PERSONA.APEMATPER)))) as PERSONA
FROM ALQUILER_DETALLE
INNER JOIN ALQUILER
ON ALQUILER.CODALQ = ALQUILER_DETALLE.CODALQ
INNER JOIN PERSONA
ON PERSONA.CODPER = ALQUILER.CODPERINQ
;

